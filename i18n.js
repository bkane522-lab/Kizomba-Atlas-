window.KizombaAtlasLanguage = {
  current: localStorage.getItem("kizomba-atlas-language") ||
    (navigator.language && navigator.language.toLowerCase().startsWith("fr") ? "fr" : "en"),

  t(key, replacements = {}) {
    const lang = this.current;
    let value = window.KIZOMBA_ATLAS_I18N[lang]?.[key] ?? window.KIZOMBA_ATLAS_I18N.fr[key] ?? key;
    Object.entries(replacements).forEach(([name, replacement]) => {
      value = value.replaceAll(`{${name}}`, String(replacement));
    });
    return value;
  },

  set(language) {
    this.current = language === "fr" ? "fr" : "en";
    localStorage.setItem("kizomba-atlas-language", this.current);
    document.documentElement.lang = this.current;
    this.apply();
    window.dispatchEvent(new CustomEvent("kizomba-atlas:languagechange", { detail: this.current }));
  },

  toggle() {
    this.set(this.current === "fr" ? "en" : "fr");
  },

  apply() {
    document.documentElement.lang = this.current;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      element.textContent = this.t(element.dataset.i18n);
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
      element.placeholder = this.t(element.dataset.i18nPlaceholder);
    });

    document.querySelectorAll("[data-i18n-title]").forEach((element) => {
      element.title = this.t(element.dataset.i18nTitle);
      element.setAttribute("aria-label", this.t(element.dataset.i18nTitle));
    });

    const languageButtons = document.querySelectorAll(".language-button");
    languageButtons.forEach((button) => {
      button.textContent = this.current.toUpperCase();
    });
  }
};

document.addEventListener("DOMContentLoaded", () => window.KizombaAtlasLanguage.apply());
